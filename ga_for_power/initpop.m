function [pop] = initpop(popsize,chromlength)
%pop=zeros(50,96);
pop=zeros(popsize,chromlength);
pop1=zeros(1,24);
pop2=zeros(1,24);
pop3=zeros(1,24);
pop4=zeros(1,24);
A=[373.6595875	364.1936534	351.3003984	355.8701597	346.4042256	354.7277194	407.7695914	506.6722819	504.7954157	491.1677347	500.7968746	510.5076173	445.7149306	477.2952451	501.0416832	511.405249	491.5757491	456.7313194	427.9255027	418.0515542	395.2843507	394.1419104	383.3703302	371.7827213
];
B=[692.8023022	663.3333845	663.7570932	652.9957477	634.7995646	652.2649418	657.9021395	674.7725418	709.5844217	710.2199847	695.5325571	698.8230762	668.0220201	686.814334	711.2038354	730.9800001	746.2275697	773.8505259	791.1352428	794.2109689	756.4475368	763.6217671	733.3808531	699.5528238
];
D=[1067.528352	1028.554565	1016.072549	1009.874773	982.184994	1007.999654	1066.737403	1182.626269	1215.594217	1202.589107	1197.525761	1210.540024	1114.850688	1165.273689	1213.457764	1243.627634	1239.041122	1231.812427	1220.279806	1213.474786	1152.883619	1158.921441	1117.867934	1072.406881
];
G1=ones(1,24);
%G2=ones(1,24);
G3=260.*ones(1,24);
G4=180.*ones(1,24);
for i=1:24
   if(A(i)<510)
       G3(i)=310/510*A(i);
       G4(i)=200/510*A(i);
   else 
        G3(i)=310*0.1*randi([9,10]);
        G4(i)=200*0.1*randi([9,10]);
   end
end

for i=1:24
       G1(i)=560/1270*B(i);
       %G2(i)=(D(i)-G3(i)*0.955-G4(i)*0.945)/0.968-G1(i);
       %G2(i)=650/1130*B(i);
end


for i=1:50
    for j=1:24    
            pop1(j)=G1(j)+randi([-20,20]);
            while(pop1(j)<150 || pop1(j)>560)
                pop1(j)=G1(j)+randi([-20,20]);
            end
    
            pop3(j)=G3(j)+randi([-20,20]);%会不会局部早熟
            while(pop3(j)<100 || pop3(j)>310)
                pop3(j)=G3(j)+randi([-20,20]);
            end
    
    
            pop4(j)=G4(j)+randi([-25,25]);%会不会局部早熟
            while(pop4(j)<0 || pop4(j)>200)
                pop4(j)=G4(j)+randi([-20,20]);
            end
            
            pop2(j)=(D(j)-pop3(j)*0.955-pop4(j)*0.945)/0.968-pop1(j);
            while( pop2(j)>710 )
                tmp=pop2(j);
                pop2(j)=710-randi([80,120]);
                pop1(j)=pop1(j)+tmp-pop2(j);
            end
    end
    pop(i,:)=[pop1,pop2,pop3,pop4];
end